// i/o işlemleri - getchar() ve putchar() ile gerçekleştirmek


#include <stdio.h>
#include <stdlib.h>
#include <math.h>


int main() {
    
    char stu_grade;
    printf("Öğrenci notunu giriniz: ");
    
    
    //getchar() ile notu al stu_grade değişkeninde sakla
    
    stu_grade = getchar();
    
    //putchar() ile notu çıktı olarak ver
    
    putchar(stu_grade);
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    return 0;
}